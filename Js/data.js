// Function to generate stars based on rating
function generateStars(rating) {
  const fullStars = Math.floor(rating);
  const halfStar = rating % 1 !== 0;
  const emptyStars = 5 - fullStars - (halfStar ? 1 : 0);
  return "★".repeat(fullStars) + (halfStar ? "☆" : "") + "☆".repeat(emptyStars);
}

// Search Filter
let inputs = document.querySelectorAll(".searchInput");
let searchBtns = document.querySelectorAll(".searchBtn");

function handleSearch(input) {
  const inputSearch = input.value.trim().toLowerCase();

  const filteredProducts = products.filter(
    (item) =>
      item.category.toLowerCase().includes(inputSearch) ||
      item.brand.toLowerCase().includes(inputSearch) ||
      item.name.toLowerCase().includes(inputSearch) ||
      item.description.toLowerCase().includes(inputSearch)
  );

  const proNotFound = document.querySelector(".notFound");
  proNotFound.style.display = filteredProducts.length === 0 ? "block" : "none";

  createProductCards(filteredProducts);
  input.value = "";
}

inputs.forEach((input, index) => {
  const searchBtn = searchBtns[index]; 

  searchBtn.addEventListener("click", () => handleSearch(input));

  input.addEventListener("keydown", (e) => {
    if (e.key === "Enter") {
      handleSearch(input);
    }
  });
});


